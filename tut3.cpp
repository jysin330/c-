#include<iostream>
using namespace std;
 int main()
{
    int num1, num2;
    cout<<"enter the value of num1:\n";
    cin>>num1;
    //'<<' is called insertion operator.
    //'>>' is called extraction operator.
    cout<<"enter the value of num2:\n";
    cin>>num2;
    cout<<"the sum of num1 and num2: "<<num1+num2;
    
    return 0;
}

